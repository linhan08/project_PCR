    private void tbAccountMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbAccountMouseClicked
        int row = tbAccount.getSelectedRow();
        if(evt.getClickCount() ==2){
            txtusername.setText(tbAccount.getValueAt(row , 1).toString());
            txtpassword.setText(tbAccount.getValueAt(row, 2).toString());
            switch(tbAccount.getValueAt(row, 3).toString()){
                case "teacher" :
                    cbRole.setSelectedIndex(0);
                    break;
                default:
                    cbRole.setSelectedIndex(1);
                    break;
            }
            textsdt.setText(tbAccount.getValueAt(row, 4).toString());
            textHoVaTen.setText(tbAccount.getValueAt(row, 5).toString());
            switch(tbAccount.getValueAt(row, 6).toString()){
                case "CNTT" :
                    cbKhoa.setSelectedIndex(0);
                    break;
                default:
                    cbKhoa.setSelectedIndex(1);
                    break;
            }
        }
    }//GEN-LAST:event_tbAccountMouseClicked


