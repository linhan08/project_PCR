    private void btAddNewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btAddNewMouseClicked
        if ( txtpassword.getText().equals("") | txtusername.getText().equals("")) {
            JOptionPane.showConfirmDialog(rootPane, "Bạn chưa nhập đủ thông tin");
        } else {
            int kt = 0;
            ControlAccount account = new ControlAccount();
            kt = account.themAccount( txtusername.getText(), 
                                    txtpassword.getText(), 
                                    cbRole.getSelectedItem().toString(),
                                    textsdt.getText().trim(),
                                    textHoVaTen.getText().trim(), 
                                    cbKhoa.getSelectedItem().toString());
            if (kt != 0) {
                model.addRow(new Object[]{
                    0 ,txtusername.getText(), txtpassword.getText(), cbRole.getSelectedItem().toString(),
                    textsdt.getText().trim(),textHoVaTen.getText().trim(), cbKhoa.getSelectedItem().toString()
                });
                JOptionPane.showMessageDialog(null, "Đăng kí thành công !", "Chúc mừng!", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Đăng kí chưa thành công !", "Đáng tiếc", JOptionPane.ERROR_MESSAGE);
            }

        }


    }//GEN-LAST:event_btAddNewMouseClicked

    private void btDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btDeleteMouseClicked
        ControlAccount account = new ControlAccount();
        int row = tbAccount.getSelectedRow();
        if(row > -1){
            account.xoaAccount((int) tbAccount.getValueAt(row, 0));
            model.removeRow(row);
        }else{
            JOptionPane.showMessageDialog(null, "Hãy chọn 1 hàng", "NOTE", JOptionPane.ERROR_MESSAGE);
        }
        

    }//GEN-LAST:event_btDeleteMouseClicked

    private void btUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdateActionPerformed
        
        ControlAccount account = new ControlAccount();
        int row = tbAccount.getSelectedRow();
        if(row > -1){
            int kt=0;
            account.xoaAccount((int) tbAccount.getValueAt(row, 0));
            kt = account.themAccount( txtusername.getText(), 
                                    txtpassword.getText(), 
                                    cbRole.getSelectedItem().toString(),
                                    textsdt.getText().trim(),
                                    textHoVaTen.getText().trim(), 
                                    cbKhoa.getSelectedItem().toString());
            model.removeRow(row);
            model.addRow(new Object[]{
                    kt ,txtusername.getText(), txtpassword.getText(), cbRole.getSelectedItem().toString(),
                    textsdt.getText().trim(),textHoVaTen.getText().trim(), cbKhoa.getSelectedItem().toString()
            });
        }else{
            JOptionPane.showMessageDialog(null, "Hãy chọn 1 hàng", "NOTE", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btUpdateActionPerformed

